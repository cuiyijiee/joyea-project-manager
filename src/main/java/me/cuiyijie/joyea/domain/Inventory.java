package me.cuiyijie.joyea.domain;

public class Inventory {


    private String materialId;
    private String accountNo;
    private String materialName;
    private String amt;
    private String price;
    private String amount;
    private String easAmt;
    private String easAmount;


    public String getMaterialId() {
        return materialId;
    }

    public void setMaterialId(String materialId) {
        this.materialId = materialId;
    }

    public String getAccountNo() {
        return accountNo;
    }

    public void setAccountNo(String accountNo) {
        this.accountNo = accountNo;
    }

    public String getMaterialName() {
        return materialName;
    }

    public void setMaterialName(String materialName) {
        this.materialName = materialName;
    }

    public String getAmt() {
        return amt;
    }

    public void setAmt(String amt) {
        this.amt = amt;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getAmount() {
        return amount;
    }

    public void setAmount(String amount) {
        this.amount = amount;
    }

    public String getEasAmt() {
        return easAmt;
    }

    public void setEasAmt(String easAmt) {
        this.easAmt = easAmt;
    }

    public String getEasAmount() {
        return easAmount;
    }

    public void setEasAmount(String easAmount) {
        this.easAmount = easAmount;
    }
}
